    private void btnCrearPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearPDFActionPerformed
        VentasVo ventaprod;
        for (int i = 0; i < TablaTodasLasVentas.getRowCount(); i++) {
            ventaprod = new VentasVo();
            ventaprod.setId(Integer.parseInt(TablaTodasLasVentas.getValueAt(i, 0).toString()));
            ventaprod.setNombre(TablaTodasLasVentas.getValueAt(i, 1).toString());
            ventaprod.setCodigo(Integer.parseInt(TablaTodasLasVentas.getValueAt(i, 2).toString()));
            ventaprod.setPrecio(Integer.parseInt(TablaTodasLasVentas.getValueAt(i, 3).toString()));
            ventaprod.setCantidad(Integer.parseInt(TablaTodasLasVentas.getValueAt(i, 4).toString()));
            ventaprod.setTotal(Integer.parseInt(TablaTodasLasVentas.getValueAt(i, 5).toString()));
            ven.add(ventaprod);
        }

        //generar ticket
        ReporteVo datos = new ReporteVo();
        datos.setGanancias(Integer.parseInt(txtGanancias.getText()));
        datos.setTotalVentas(Integer.parseInt(txtTotalDeVentas.getText()));
        datos.setParesVendidos(Integer.parseInt(txtTotalDeParesVendidos.getText()));
        rep.add(datos);

        String f = fecha.getMesActual() + fecha.getHora() + fecha.getMinuto();
        ticket.generarTicket((f), datos,ven);
    }//GEN-LAST:event_btnCrearPDFActionPerformed


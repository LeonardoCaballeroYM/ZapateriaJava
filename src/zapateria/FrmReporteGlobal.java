    public void llenarT() {
        try {
            LimpiarTabla();
            Statement st = con.conexion().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM vntprod");
            while (rs.next()) {
                Object[] dat = new Object[6];
                dat[0] = rs.getInt(1);//id
                dat[1] = rs.getString(2);//producto
                dat[2] = rs.getInt(3);//codigo
                dat[3] = rs.getString(4);//precio
                dat[4] = rs.getInt(5);//cantidad
                dat[5] = rs.getInt(6);//total
                llenar.addRow(dat);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

    public void LimpiarTabla() {
        for (int i = TablaTodasLasVentas.getRowCount() - 1; i >= 0; i--) {
            llenar.removeRow(i);
        }
    }

    private void suma() {
        txtGanancias.setText("0");
        txtTotalDeParesVendidos.setText("0");
        int ta = llenar.getRowCount();
        int c = 0;
        do {
            try {
                int f = c++;
                int totales = Integer.parseInt(llenar.getValueAt(f, 5).toString());
                String ganancias = txtGanancias.getText();
                int nu2 = Integer.parseInt(ganancias);
                long re = totales + nu2;

                int par = Integer.parseInt(llenar.getValueAt(f, 4).toString());
                String pares = txtTotalDeParesVendidos.getText();
                int nu = Integer.parseInt(pares);
                long ParesVendidos = par + nu;

                txtGanancias.setText(String.valueOf(re));
                txtTotalDeVentas.setText(String.valueOf(f + 1));
                txtTotalDeParesVendidos.setText(String.valueOf(ParesVendidos));

            } catch (NumberFormatException e) {
                System.out.println(e);
            }

        } while (c < ta);

    }


    public void llenarT() {
        try {
            LimpiarTabla();
            Statement st = con.conexion().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM producto");
            while (rs.next()) {
                Object[] dat = new Object[7];
                dat[0] = rs.getInt(1);//id
                dat[1] = rs.getInt(2);//codigo
                dat[2] = rs.getString(3);//producto
                dat[3] = rs.getString(4);//modelo
                dat[4] = rs.getInt(5);//precio
                dat[5] = rs.getInt(6);//existencias
                dat[6] = rs.getString(7);//marca
                llenar.addRow(dat);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    
    public void LimpiarTabla() {
        for (int i = tablaInventario.getRowCount() - 1; i >= 0; i--) {
            llenar.removeRow(i);
        }
    }

     private void suma() {
        txtSumaExistencias.setText("0");
        txtTotaldeproductos.setText("0");
        int ta = llenar.getRowCount();
        int c = 0;
        do {
            try {
                int f = c++;
                int totales = Integer.parseInt(llenar.getValueAt(f, 5).toString());
                String ganancias = txtSumaExistencias.getText();
                int nu2 = Integer.parseInt(ganancias);
                long re = totales + nu2;

                int par = Integer.parseInt(llenar.getValueAt(f, 4).toString());
                String pares = txtTotaldeproductos.getText();
                int nu = Integer.parseInt(pares);
                long ParesVendidos = par + nu;

                txtSumaExistencias.setText(String.valueOf(re));
                txtTotaldeproductos.setText(String.valueOf(f + 1));

            } catch (NumberFormatException e) {
                System.out.println(e);
            }

        } while (c < ta);

    }
